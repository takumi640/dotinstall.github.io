'use strict'

{
  const next13 = document.getElementById('next13');
  const set13 = document.getElementById('set13');
  const back13 = document.getElementById('back13');

  next13.addEventListener('mouseover', () =>{
    set13.style.transform = 'translateX(-100%)';
  });

  back13.addEventListener('mouseout', () =>{
    set13.style.transform = 'translateX(0px)';
  });

}

